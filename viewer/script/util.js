// util
function p(str) {
    window.document.write(str)
}
function pln(str) {
    window.document.writeln(str)
}
function br() {
    pln("<br>")
}
function i(str) {
    return '<img src="' + str + '.gif">' 
}
function image(name) {
    return window.document.images[name]
}
